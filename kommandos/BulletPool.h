#pragma once
#include "Bullet.h"
#include <list>

using namespace std;

///-------------------------------------------------------------------------------------------------
/// <summary>	A bullet pool. </summary>
///-------------------------------------------------------------------------------------------------

class BulletPool
{
public:

	///-------------------------------------------------------------------------------------------------
	/// <summary>	Delete copy constructor. </summary>
	///-------------------------------------------------------------------------------------------------

	BulletPool(const BulletPool&) = delete;

	///-------------------------------------------------------------------------------------------------
	/// <summary>	Delete assignment operator. </summary>
	///-------------------------------------------------------------------------------------------------

	BulletPool& operator=(const BulletPool&) = delete;

	///-------------------------------------------------------------------------------------------------
	/// <summary>	Gets the instance. </summary>
	///-------------------------------------------------------------------------------------------------

	static BulletPool* GetInstance();

	///-------------------------------------------------------------------------------------------------
	/// <summary>	Gets the resource. </summary>
	///-------------------------------------------------------------------------------------------------

	Bullet* GetResource();

	///-------------------------------------------------------------------------------------------------
	/// <summary>	Returns a resource. </summary>
	///-------------------------------------------------------------------------------------------------

	void ReturnResource(Bullet* object);
private:

	///-------------------------------------------------------------------------------------------------
	/// <summary>	Default constructor. </summary>
	///-------------------------------------------------------------------------------------------------

	BulletPool();
	/// <summary>	The resources. </summary>
	std::list<Bullet*> resources;
	/// <summary>	The instance. </summary>
	static BulletPool* instance;
};
